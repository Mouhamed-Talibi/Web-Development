# Common Errors : 
"""                                            the building bloque of a program :

Syntax errors -> These are the first errors you will make and the easiest to fix. A syntax error means that you have violated the “grammar” 
                rules of Python.
                
Logic errors  -> A logic error is when your program has good syntax but there is a mistake in the order of the statements or perhaps a mistake in 
                how the statements relate to one another. 

Semantic errors ->  A semantic error is when your description of the steps to take is syntactically perfect and in the right order, but there is 
                    simply a mistake in the program               
"""

